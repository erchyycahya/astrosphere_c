# Membuat median nilai 0 - 100

Angka = 91
if(Angka >=91 and Angka <=100) :
    print("A+")
elif(Angka >=84 and Angka <=90) :
    print("A")

elif(Angka >=77 and Angka <=83) :
    print("B+")

elif(Angka >=71 and Angka <=76) :
    print("B")

elif(Angka >=66 and Angka <=70) :
    print("B-")

elif(Angka >=61 and Angka <=65) :
    print("C+")

elif(Angka >=55 and Angka <=60) :
    print("C-")

elif(Angka >=41 and Angka <=54) :
    print("D")

else:
    print("E")